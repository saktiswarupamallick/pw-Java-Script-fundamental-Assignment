//         Answer2: Static vs.Dynamic:

// HTML:

// HTML provides the static structure of a webpage and is used to define the layout and content.
//     It's primarily concerned with how information is organized and presented on the page.
// JavaScript:

// JavaScript adds dynamic behavior to HTML by allowing developers to respond to events, modify the DOM, and update content dynamically.
// It enables the creation of interactive features such as form validation, image sliders, and real - time updates.
// Execution Context:

// HTML:

// HTML is parsed and executed by the browser in a linear fashion from top to bottom.
// It defines the structure of the page and the initial content.
//     JavaScript:

// JavaScript is executed in response to events or as part of the page loading process.
// It can be placed within the HTML document, either inline or in external files, and is executed when the browser encounters the < script > tags.
//     Syntax:

// HTML:

// HTML uses tags and attributes to define elements and their properties.
// It has a more declarative syntax focused on structure and semantics.
//     JavaScript:

// JavaScript uses a more imperative syntax with statements, variables, functions, and objects.
// It is a scripting language that allows developers to write procedural and object - oriented code

